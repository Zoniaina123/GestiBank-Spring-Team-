package com.wha.springmvc.model;

public class Auth {
	
	private String userU;
	private String pwd;

	public Auth() {
		
	}

	public String getUser() {
		return userU;
	}

	public void setUser(String userU) {
		this.userU = userU;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	

}
